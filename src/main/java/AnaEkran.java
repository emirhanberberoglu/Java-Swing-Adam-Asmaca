    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (secilenKelime.isEmpty()) {
            return;
        }

        String hamTahmin = txtHarf.getText().trim();
        txtHarf.setText("");

        if (hamTahmin.isEmpty()) {
            return;
        }

        String tahmin = hamTahmin.toUpperCase(java.util.Locale.forLanguageTag("tr-TR"));

        char harf = tahmin.charAt(0);
        boolean bulunduMu = false;
        boolean oyunBittiMi = true;

        java.awt.Component[] bilesenler = pnlKelime.getComponents();

        for (int i = 0; i < secilenKelime.length(); i++) {
            if (secilenKelime.charAt(i) == harf) {
                bulunduMu = true;
                ((javax.swing.JLabel) bilesenler[i]).setText(String.valueOf(harf));
            }
            if (((javax.swing.JLabel) bilesenler[i]).getText().equals("*")) {
                oyunBittiMi = false;
            }
        }

        if (bulunduMu) {
            if (oyunBittiMi) {
                oyunuBitir(true);
            }
        } else {
            hataSayisi++;
            resimGuncelle();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

