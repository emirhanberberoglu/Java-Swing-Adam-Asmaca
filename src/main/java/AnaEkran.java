    private void loglariDoldur() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            java.io.File dosya = new java.io.File(TXT_YOLU + "log.txt");
            if (dosya.exists()) {
                java.util.Scanner okuyucu = new java.util.Scanner(dosya);
                while (okuyucu.hasNextLine()) {
                    String satir = okuyucu.nextLine();
                    model.addRow(new Object[]{satir});
                }
                okuyucu.close();
            }
        } catch (Exception e) {
            System.out.println("Log dosyası okunamadı.");
        }
    }

