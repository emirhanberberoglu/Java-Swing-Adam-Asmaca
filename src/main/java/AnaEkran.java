    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String girilenSifre = javax.swing.JOptionPane.showInputDialog(this, "Logları silmek için şifrenizi giriniz:");

        if (girilenSifre == null || girilenSifre.isEmpty()) {
            return;
        }

        try {
            java.io.File sifreDosyasi = new java.io.File(TXT_YOLU + "sifre.txt");
            java.util.Scanner okuyucu = new java.util.Scanner(sifreDosyasi);
            String kayitliSifre = "";
            if (okuyucu.hasNext()) {
                kayitliSifre = okuyucu.next();
            }
            okuyucu.close();

            if (girilenSifre.equals(kayitliSifre)) {
                java.io.FileWriter yazar = new java.io.FileWriter(TXT_YOLU + "log.txt");
                yazar.close();

                loglariDoldur();
                javax.swing.JOptionPane.showMessageDialog(this, "Loglar başarıyla temizlendi!");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Hatalı şifre girdiniz! Temizleme işlemi iptal edildi.");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "İşlem sırasında bir hata oluştu!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

